                            <h3 class="margin-top-20"> Cum protejam animalele de companie de purici si capuse ? </h3>
                           <h4>Amenajeaza și întretine cat mai bine gradina</h4>
                            <p>Primul pas pe care îl poți face este să îți aperi proprietatea de invazia puricilor și capușelor. Dacă stai la casă și ai grădină, acordă o mare importantă întreținerii acesteia. Vegetația bogată și iarba înaltă sunt locuri care favorizează apariția paraziților, de aceea trebuie să tunzi regulat gazonul și să cureți resturile de vegetație. Cu cât spațiul din grădină este mai liber și mai aerist cu atât mediul va fi mai puțin propice înmulțirii dăunătorilor. Lumina soarelui și curenții de aer din grădină distrug rapid larvele de purici.</p>
                            <h4>Prevenția, esențială pentru sănătatea animalelor</h4>
                            <p>Baia periodică a animalului de companie cu șampon împotriva puricilor și căpușelor, periatul zilnic și verificarea blănii de fiecare dată când vine de afară sau de la joacă sunt alți pași importanți în prevenție, pe care stăpânii de animale de companie ar trebui să îi urmeze pentru a avea companioni cât mai sănătoși și veseli.</p>
                            <h4>Păstrează curățenia în casă</h4>
                            <p>A avea purici și căpușe în casă nu înseamnă că locuința ta este murdară. Dar dacă acorzi o atenție deosebită anumitor zone, poți face dăunătorii mai puțin bineveniți. De exemplu, covorul este un loc în care puricii și căpușele se instalează frecvent, așadar acesta trebuie aspirat cel puțin o dată pe săptămâna. La fel de important este ca periodic să speli și să cureți toate obiectele care îi aparțin câinelui sau pisicii. Culcușul, jucăriile, bolurile de mâncare sunt locuri care pot ajunge să fie invadate de dăunătorii care mai apoi se vor muta în blana animalului tău de companie.</p>
                            <h4>Zgarda antiparazitară, cel mai simplu și eficient mod de prevenție</h4>
                            <p>Sunt multe metode disponibile pe piață care te ajută să ții la distanță puricii și căpușele de blana animalului tău de companie. În funcție de rasă și stilul de viață, greutate, mediul în care trăiește un câine sau o pisică, o anumită metodă de prevenție și tratament este mai eficientă. Însă zgarda antiparzitară rămâne în topul preferințelor celor mai mulți proprietari de animale de companie. Pentru ca un câine sau o pisica să poată purta acest dispozitiv nu este nevoie nici să consulți medicul veterinar și nici să îți faci probleme că ar interfera cu alte medicamente sau soluții împotriva căpușelor și puricilor. </p>
