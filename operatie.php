                            <h3 class="margin-top-20">Pregatirea animalului tau de companie </h3>
                            <p class="margin-top-20">Este important ca cel care se prezintă la clinică cu animalul să fie proprietarul acestuia şi să fi îndeplinit vârstă majoratului. Cu 12 ore înainte de operaţie animalul nu va mă fi hrănit, acesta având la discreţie doar apă de băut, până cu 2-3 ore înainte de programare. Motivul pentru care animalul nu are voie să mănânce înainte de operaţie este acela că sub efectul anesteziei poate vomita, existând riscul de a se îneca.</p>
                            <p class="margin-top-20">Este recomandat ca pisicile să fie ţinute în casă în noaptea de dinanintea operaţiei pentru a le împiedica să caute mâncare în altă parte. Câinii vor fi plimbaţi înainte de a ajunge la clinică pentru a-şi face nevoile. Este bine ca înainte de orice intervenţie chirurgicală animalul să fie spălat pentru ca după operaţie următoarea îmbăiere poate avea loc după cel puţin 14 zile.</p>
                            <p class="margin-top-20">Când ajungi la clinică vei avea ocazia de a discuta cu personalul clinicii toate preocupările pe care le ai cu privire la intervenţia chirurgicală, ţi se va explica procedura şi eventualele riscuri, vei lăsa datele tale de contact, vei fi informat asupra costului intervenţiei şi vei completa un formular de consimţământ.</p>
                            <p class="margin-top-20">Va trebui să informezi personalul medical dacă animăluţul tău este în cursul unui tratament medicamentos anume sau dacă a mai fost supus vreunei intervenţii chirurgicale în trecut. Înainte de administrarea anestezicului animalului tău i se vor face nişte ţeste de sânge pentru a stabili dacă rinichii şi ficatul sau funcţionează normal. Este foarte important că aceste organe să funcţioneze corect pentru că ele sunt principalele organe care filtrează anestezicul folosit în timpul operaţiei.</p>
                            <p class="margin-top-20">Animalele ale căror organe nu funcţionează corect vor prezenţa simptome abia atunci când afecţiunile sunt foarte grave. Administrarea anestezicelor animalelor cu probleme renale sau hepatice este foarte periculoasă pentru ele pentru că la scurt timp de la operaţie pot dezvoltă insuficiente hepatice sau renale, care pot fi fatale. În plus, sângele animalului tău va fi testat pentru depistarea anemiei, deshidratării, infecţiilor sau a timpului de coagulare şi eventualele parazitoze (dirofilaria..). Aceste ţeste sunt importante atât atunci când animalul este tânăr, dar mai ales atunci când acesta este în vârstă.</p>
