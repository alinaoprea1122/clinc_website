                            <p class="margin-top-20">Pentru a proteja plagă rezultată în urmă operaţiei, medicul va recomada purtarea unui guler Elisabetan. Acesta trebuie să fie potrivit pentru dimensiunea animalului şi trebuie purtat până când firele de sutura de la nivelul pielii vor fi îndepărtate. În acest fel se previn complicaţiile postoperatorii şi infecţiile.</p>
